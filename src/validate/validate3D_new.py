from shapely.geometry import Polyhedron

# 定义立方体的8个顶点（按顺序）
vertices = [
    (1736.596, 959.587, 79.548),   # 下前左
    (1988.506, 959.587, 79.548),   # 下前右
    (1988.506, 1165.064, 79.548),  # 下后右
    (1736.596, 1165.064, 79.548),  # 下后左
    (1736.596, 959.587, 143.033),  # 上前左
    (1988.506, 959.587, 143.033),  # 上前右
    (1988.506, 1165.064, 143.033), # 上后右
    (1736.596, 1165.064, 143.033)  # 上后左
]

# 定义6个面（每个面由4个顶点索引组成，按逆时针顺序）
faces = [
    [0, 1, 2, 3],  # 下底面（Z=79.548）
    [4, 5, 6, 7],  # 上底面（Z=143.033）
    [0, 1, 5, 4],  # 前面
    [1, 2, 6, 5],  # 右面
    [2, 3, 7, 6],  # 后面
    [3, 0, 4, 7]   # 左面
]

# 构建三维多面体
polyhedron = Polyhedron(vertices, faces)
print(polyhedron.is_valid)  # 输出 True，纯三维校验无歧义